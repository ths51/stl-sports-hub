function getColorIndex(text) {
    let colorIndex = 0
    for (let num = 0; num < allData.MLScolors.length; num++) {
        var currOpp = allData.MLScolors[num].team
        if (text == currOpp) {
            // console.log(currOpp)
            colorIndex = num
            break
        }
    }
    return colorIndex
}

